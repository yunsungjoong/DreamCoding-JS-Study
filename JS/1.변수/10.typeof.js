// typeof : 데이터 타입을 확인
// 값을 타입 문자열 반환 

let variable;
console.log(typeof variable);

variable = ''; // 할당된 값에 따라 타입이 결정된다.
console.log(typeof variable);

console.log(typeof variable);

variable = {};
console.log(typeof variable);

variable = function() {};


// 자바스크립트에도 타입이 존재한다.
